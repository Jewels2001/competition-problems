int main() {
    ios::sync_with_stdio(0); cin.tie(0);

    int n;
    cin >> n;

    vector<int> res;
    int ans = 0;
    set<int> s;

    for(int i=0; i<n; i++) {
      int temp;
      cin >> temp;
      res.push_back(temp);
    }

    for(int i=0; i<n; i++) {
      set<int>::iterator it=s.find(res[i]);
      if(it == s.end()) {
        ans += res[i]+1;
        s.insert(res[i]);
      }
    }
    cout << ans << endl;

}
