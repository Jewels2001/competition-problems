int main() {
    ios::sync_with_stdio(0); cin.tie(0);
    int n;
    vector<int> outcomes;

    set<int> dice;
    int ans = 0;
    int roll = 0;

    cin >> n;
    for(int i=0; i<n; i++) {
      int temp;
      cin >> temp;
      outcomes.push_back(temp);
    }

    for(int i=0; i<n; i++) {
      set<int>::iterator it=dice.find(outcomes[i]);
      if(it != dice.end()) {
        if(roll == outcomes[i]) {
          ans = 0;
          cout << "i:" << i << " ans: " << ans << endl;
        }
      }
      else {
        dice.insert(outcomes[i]);
        if(outcomes[i] > roll) {
          roll = outcomes[i];
          ans = max(ans, i+1);
        }

        cout << "i:" << i << " ans: " << ans << endl;

      }

    }

    if(ans) {
      cout << ans << endl;
    }
    else {
      cout << "none" << endl;
    }
    // cout << (ans == 0) ? ans : "none" << endl;

}
