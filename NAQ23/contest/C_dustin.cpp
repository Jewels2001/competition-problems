int main() {
    string a,b; cin>>a>>b;

    sort(all(a));
    sort(all(b));

    string ans = "";
    int pa=0,pb=0;
    while(pa < sz(a) && pb < sz(b)) {
        if(a[pa] < b[pb]) {
            ans += a[pa];
            pa++;
        } else {
            ans += b[pb];
            pb++;
        }
    }

    if(pa != sz(a))
        ans += a.substr(pa);
    if(pb != sz(b))
        ans += b.substr(pb);

    cout<<ans<<endl;
}
