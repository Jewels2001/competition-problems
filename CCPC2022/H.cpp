string runes = "!\"#$%&'()*+,-./";

int main() {
    char r,l; cin>>r>>l;
    int offset = (l-'a') - (r-'!');

    cin.ignore();
    string line;
    while(getline(cin,line)) {
        istringstream iss(line);
        string word;
        while(iss>>word) {
            if(sz(word) == 1) {
                if(word[0] == '0')
                    cout<<" ";
                else if(word[0] == '<')
                    cout<<",";
                else if(word[0] == '>')
                    cout<<".";
                else {
                    int o = (word[0]-'!')+offset;
                    o = (o+26)%26;
                    cout<<(char)(o+'a');
                }
            }
            else {
                int sum = 0;
                for(char c:word) {
                    if(c == '\n') continue;
                    sum += c-32;
                }
                int o = (sum-1)%26;
                o+=offset;
                o = (o+26)%26;
                cout<<(char)(o+'a');
            }
        }
        cout<<endl;
    }
}
